export const readingTimeText = (language, readingTime) => {
  if (locale(language) !== "ES") {
    return readingTime;
  }

  if (readingTime === "N/A") {
    return "No disponible";
  }

  return readingTime
    .replace(/^approx\s+/i, "Aprox. ")
    .replace(/\bminutes\b/i, "minutos")
    .replace(/\bminute\b/i, "minuto")
    .replace(/\bmins\b/i, "min");
};

export const contributionTooltipText = (language, count, date) => {
  if (locale(language) === "ES") {
    return `${count} contribuci${count === 1 ? "\u00f3n" : "ones"} el ${date}`;
  }

  return `${count} contribution${count === 1 ? "" : "s"} on ${date}`;
};

